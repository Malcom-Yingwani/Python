# Tuples and updating tuples

menu = ('chicken', 'beef', 'pork', 'turkey', 'lamb')

print('These are the menu options:')
for item in menu:
    print(item)

print('\n')


menu = ('chicken', 'beef', 'pork', 'goat', 'duck')

print('These are the updated menu options:')
for item in menu:
    print(item)
